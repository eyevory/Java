
public class javaFunctions {

    /**
     * @param args the command line arguments
     */
    public static void main(String args[]) {
        // Variables(String, int, float, char, booll
        String name = "John Lennon";
        System.out.println(name);
        
        // int Variable
        int num = 15;
        System.out.println(num);
        
        // Can give value to a variable later in the code
        int num2;
        num2 = 727;
        System.out.println(num2);
        
        // Can change value of variable after it's been given one
        num2 = 1;
        System.out.println(num2);
        
        /*
        Cannot change value if the int is a final int (gives error)
        final int num3 = 11;
        num3 = 13;
        */
        
        // float Variables fractions
        float floatnum = 3.141f;
        System.out.println(floatnum);
        
        // Boolean true or false statement, 1 or 0
        boolean Bool = true;
        System.out.println(Bool);
        
        // char Variable
        char char1 = 'P';
        System.out.println(char1);
        
    }
}
