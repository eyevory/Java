
public class javaOperators {


    public static void main(String args[]) {
       int sum1 = 50;
       int sum2 = sum1 + 250;
       int sum3 = sum2 + sum1;
       
       // Print sums with math
        System.out.println(sum1 + sum2);
        System.out.println(sum1 - sum2);
        System.out.println(sum1 * sum2);
        System.out.println(sum1 / sum2);
        System.out.println(sum1 % sum2);
        System.out.println(sum3);
        
       int x = 5;
       int y = 5;
       
       // Add 1 and reduce by 1
       ++x;
       --y;
        System.out.println(x);
        System.out.println(y);
    }
}
